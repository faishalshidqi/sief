class Supplier {
  final String name;
  final String phone;
  final String docId;
  final String address;
  final DateTime addedAt;
  final DateTime updatedAt;

  Supplier({
    required this.name,
    required this.phone,
    required this.docId,
    required this.address,
    required this.addedAt,
    required this.updatedAt,
  });
}
